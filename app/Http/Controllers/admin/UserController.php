    public function destroy($id)
    {
        $data=User::find($id);
        $file_path = public_path().'/storage/'.$data->profile_photo_path;
        if(File::exists($file_path))
        {
         File::delete($file_path);
        }
        $data->delete();
        return back()->with('success', 'Course has deleted Successfully');
